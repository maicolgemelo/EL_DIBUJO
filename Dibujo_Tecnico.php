               <p>
                El <b>dibujo artístico</b> es la disciplina del trazado y delineado de cualquier figura, abstracta o que represente un objeto real, 
                como forma de expresión gráfica. Desde la Prehistoria ha funcionado como lenguaje alternativo de ámbito universal que permite la transmisión de información, 
                sean ideas, descripciones o sentimientos.
