            <p>
                El <b>dibujo técnico</b> es un sistema de representación gráfica de diversos tipos de objetos, 
                con el propósito de proporcionar información suficiente para facilitar su análisis, ayudar a elaborar su diseño y posibilitar su futura construcción y mantenimiento.
                 Suele realizarse con el auxilio de medios informatizados o, directamente, sobre el papel u otros soportes planos.
                 <br>
                 La representación gráfica se basa en la geometría descriptiva y utiliza las proyecciones ortogonales para dibujar las distintas vistas de un objeto.
               <p>
                Los objetos, piezas, máquinas, edificios, planos urbanos, entre otros , se suelen representar en planta (vista superior, vista de techo, planta de piso, cubierta, entre otros ),
                alzado (vista frontal o anterior) y lateral (acotaciones); son necesarias un mínimo de dos proyecciones (vistas del objeto) para aportar información útil del objeto, dependiendo esto de la complejidad del mismo.
                Las vistas mencionadas de acuerdo al sistema ortogonal se llaman fundamentales por pertenecer al triedro fundamental, este triedro lo conforman el plano anterior, superior y lateral.
               </p>
               <p>
                Con el objetivo de unificar el lenguaje del dibujo técnico se establecieron normativas aprobadas internacionalmente, 
                pero cada país tiene su organismo nacional de normalización para el estudio y aprobación de las diferentes Normas (en Argentina es IRAM la única organización que realiza esta tarea).
                Aplicando estas normativas cualquier plano podrá ser interpretado por cualquier profesional del área correspondiente.
               </p>
    
