            <p>
                El dibujo sirve como una herramienta para la representación de objetos reales o ideas que, a veces,
                no es posible expresar fielmente con palabras (o único medio de expresión ante la carencia de escritura). 
                Los primeros dibujos conocidos se remontan a la prehistoria, teniendo 73 000 años la primera muestra.
                Entre las pinturas rupestres destaca la cueva de Altamira, donde el ser humano plasmó en los techos y 
                paredes de las cavernas lo que consideraba importante transmitir o expresar, normalmente actividades relacionadas con su forma de vida y su entorno.
               
            </p>
               <p>
                De las primeras civilizaciones perduran escasos ejemplares de dibujos, normalmente, 
                por la fragilidad del material en el que fueron ejecutados (se han hallado en ostraca y pinturas murales inacabadas), 
                o porque eran un medio para elaborar pinturas posteriormente, recubiertos con capas de color. 
                Las culturas de la Antigua China, Mesopotamia, el valle del Indo o el Antiguo Egipto dejaron muestras claras de ello, 
                ideando los primeros cánones de proporciones, como sucedió también en la Antigua Grecia y Roma.
               </p>
    
               <p>
                En la Edad Media se utilizó profusamente el dibujo, generalmente coloreado, 
                para representar sobre pergaminos temas religiosos a modo de explicación o alegoría de las historias escritas, 
                privando así lo simbólico sobre lo realista, incluso las proporciones y cánones de la época. 
                La cultura islámica también contribuyó con preciosos dibujos que solían acompañar textos de anatomía, 
                astronomía o astrología.
               </p>
                <p>
                    Es en el Renacimiento cuando el dibujo eclosiona, logrando alcanzar sublimes logros. 
                    Por primera vez se estudia el método de reflejar la realidad con la mayor fidelidad posible, 
                    con arreglo a normas matemáticas y geométricas impecables: con Filippo Brunelleschi surge la perspectiva cónica. 
                    El dibujo, de la mano de los grandes artistas renacentistas cobra autonomía, 
                    adquiriendo valor propio en autorretratos, planos arquitectónicos y variados temas realistas 
                    –como los de Leonardo da Vinci–, además servir como estudio previo imprescindible de otras artes, 
                    como la pintura, escultura o arquitectura.
               </p>
    
    
